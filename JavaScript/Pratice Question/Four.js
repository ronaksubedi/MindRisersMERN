let studentMarks = 90;
if (studentMarks >= 90 && studentMarks <= 100) {
    console.log("Grade A");
} else if (studentMarks >= 70 && studentMarks < 90) {
    console.log("Grade B");
} else if (studentMarks >= 60 && studentMarks < 70) {
    console.log("Grade C");
} else if (studentMarks >= 50 && studentMarks < 60) {
    console.log("Grade D");
}
else {
    console.log("Grade F");
}